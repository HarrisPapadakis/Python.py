# Εισάγουμε τη βιβλιοθήκη periodictable που περιέχει πληροφορίες για τα χημικά στοιχεία
import periodictable

# Ζητάμε από τον χρήστη να δώσει το όνομα ενός στοιχείου
# Η μέθοδος .capitalize() φροντίζει να ξεκινάει με κεφαλαίο το όνομα (π.χ. "Oxygen")
element_name = input("Δώσε το όνομα του στοιχείου: ").capitalize()

# Χρησιμοποιούμε τη getattr για να πάρουμε το στοιχείο από τη βιβλιοθήκη
element = getattr(periodictable, element_name, None)

# Αν το στοιχείο βρέθηκε στη βιβλιοθήκη
if element:
    # Εκτύπωση βασικών πληροφοριών για το στοιχείο
    print(f"Στοιχείο: {element.name}")               # Όνομα στοιχείου
    print(f"Σύμβολο: {element.symbol}")              # Χημικό σύμβολο
    print(f"Ατομικός αριθμός: {element.number}")     # Ατομικός αριθμός
    print(f"Ατομικό βάρος: {element.mass}")          # Ατομικό βάρος
    print(f"Πυκνότητα: {element.density} g/cm³")     # Πυκνότητα (αν υπάρχει)
else:
    # Αν το στοιχείο δεν βρέθηκε, εμφανίζουμε σχετικό μήνυμα
    print(f"Το στοιχείο '{element_name}' δεν βρέθηκε.")
